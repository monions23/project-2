var expanded = false;

function toggleCounties() {
  var checkboxes = document.getElementById("county-list");
  if (!expanded) {
    checkboxes.style.display = "block";
    expanded = true;
  } else {
    checkboxes.style.display = "none";
    expanded = false;
  }
}

function toggleData() {
  var checkboxes = document.getElementById("data-list");
  if (!expanded) {
    checkboxes.style.display = "block";
    expanded = true;
  } else {
    checkboxes.style.display = "none";
    expanded = false;
  }
}
